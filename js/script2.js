            document.addEventListener('DOMContentLoaded', function() {
            const paintingItems = document.querySelectorAll('.painting-item');
            const modalOverlay = document.getElementById('modalOverlay');
            const closeModalBtn = document.getElementById('closeModal');
            
            // Элементы модального окна
            const modalImg = document.getElementById('modalImg');
            const modalTitle = document.getElementById('modalTitle');
            const modalAuthor = document.getElementById('modalAuthor');
            const modalYear = document.getElementById('modalYear');
            const modalDescription = document.getElementById('modalDescription');
            
            // Функция открытия модального окна
            function openModal(paintingId) {
                const painting = paintingsData[paintingId];
                if (!painting) return;
                
                // Заполняем данные
                modalImg.src = painting.image;
                modalImg.alt = painting.title;
                modalTitle.textContent = painting.title;
                modalAuthor.textContent = painting.author;
                modalYear.textContent = painting.year;
                modalDescription.textContent = painting.description;
                
                // Показываем модальное окно
                modalOverlay.style.display = 'flex';
                document.body.style.overflow = 'hidden'; // Отключаем прокрутку фона
            }
            
            // Функция закрытия модального окна
            function closeModal() {
                modalOverlay.style.display = 'none';
                document.body.style.overflow = 'auto'; // Включаем прокрутку обратно
            }
            
            // Обработчики для миниатюр
            paintingItems.forEach(item => {
                const paintingId = item.getAttribute('data-painting');
                
                item.addEventListener('click', () => {
                    openModal(paintingId);
                });
            });
            
            // Закрытие по крестику
            closeModalBtn.addEventListener('click', closeModal);
            
            // Закрытие по клику на оверлей
            modalOverlay.addEventListener('click', (e) => {
                if (e.target === modalOverlay) {
                    closeModal();
                }
            });
            
            // Закрытие по нажатию Escape
            document.addEventListener('keydown', (e) => {
                if (e.key === 'Escape' && modalOverlay.style.display === 'flex') {
                    closeModal();
                }
            });
        });